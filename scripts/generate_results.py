#!/usr/bin/env python3
"""
Generate docs/formation_flight/results.md from current model state.

Run this after any model changes to update the documented results.
The output is a reproducible snapshot — re-run to see how results
change as models improve.

Usage:
    python scripts/generate_results.py
"""

import datetime

from wingz.evaluation.solver import solve_converged, find_max_payload
from wingz.constants import (
    STALL_MARGIN_DAY, STALL_MARGIN_NIGHT,
    PANEL_EFFICIENCY, PANEL_COVERAGE,
    PROPULSION_EFFICIENCY,
    BATTERY_ENERGY_DENSITY,
    CD0, OSWALD_E,
    DEFAULT_LATITUDE, DEFAULT_DAY_OF_YEAR,
    N_SERVOS, SERVO_POWER_ACTIVE, SERVO_BASE_DUTY,
)
from wingz.cost.materials import fleet_cost
from wingz.structures.empirical import EmpiricalStructure, SOLAR_HALE_DATA


# (N, span_m) configs for reference comparisons
REFERENCE_CONFIGS = [
    (1, 60),
    (2, 40),
    (2, 30),
    (4, 20),
    (4, 15),
    (6, 10),
    (6, 15),
]


def fmt(val, decimals=1):
    if val is None:
        return "—"
    return f"{val:.{decimals}f}"


def fmt_k(val):
    """Format as $Xk or $XM."""
    if val is None:
        return "—"
    if val >= 1_000_000:
        return f"${val / 1_000_000:.1f}M"
    return f"${val / 1_000:.0f}k"


def main():
    structure = EmpiricalStructure.from_data(SOLAR_HALE_DATA)
    now = datetime.datetime.now().strftime("%Y-%m-%d %H:%M")

    lines = []
    lines.append("# Formation Flight — Model Results")
    lines.append("")
    lines.append(f"*Auto-generated by `scripts/generate_results.py` on {now}.*")
    lines.append("*Re-run after model changes to update.*")
    lines.append("")

    # ── 1. Structural Model ──────────────────────────────────────────────────
    lines.append("## Structural Model")
    lines.append("")
    lines.append(
        f"Power-law fit (empirical, calibrated to solar HALE data): "
        f"`m_wing = {structure.coefficient:.6f} × b^{structure.exponent:.3f}`"
    )
    lines.append(f"- Data range: {structure.min_span:.0f}–{structure.max_span:.0f} m span")
    lines.append(f"- Exponent {structure.exponent:.3f} — superlinear scaling confirms structural penalty at large spans")
    lines.append(f"- All wing-mass data points are estimated from published MTOW and typical structural fractions")
    lines.append("")
    lines.append("The solver uses the **beam** structural model (`wingz/structures/beam.py`),")
    lines.append("not this empirical fit. The empirical fit is shown here for reference and cross-check.")
    lines.append("")

    # ── 2. Reference Comparisons ─────────────────────────────────────────────
    lines.append("## Reference Comparisons")
    lines.append("")
    lines.append(
        "Max-payload results via `find_max_payload(N, span)`. "
        "Beam structural model, 30°N summer solstice, V formation 10% overlap."
    )
    lines.append("")

    headers = [
        "Config", "N", "AR", "Chord(m)",
        "Struct/ac(kg)", "Payload/ac(W)", "Batt/ac(kg)", "AC mass(kg)",
        "Fleet mass(kg)", "Pld power(W)", "Cost", "$/kg pld",
    ]
    lines.append("| " + " | ".join(headers) + " |")
    lines.append("| " + " | ".join(["---"] * len(headers)) + " |")

    for N, span in REFERENCE_CONFIGS:
        r = find_max_payload(N, span)
        if r is None:
            label = f"{N}×{span}m"
            lines.append(f"| {label} | {N} | — | — | — | — | — | — | — | — | — | — |")
            continue

        label = f"{N}×{span}m"
        chord = r["chord"]
        struct_ac = r["struct_each"]
        pld_power_ac = r["payload_power"] / N if N > 0 else r["payload_power"]
        batt_ac = r["batt_each"]
        ac_mass = r["ac_mass"]
        fleet_mass = r["fleet_mass"]
        pld_power_total = r["payload_power"]
        cost = r["cost"]

        # $/kg payload: payload_mass_total = pld_power_total * 50g/W / 1000
        pld_mass_total = r.get("payload_mass_total", 0.0)
        dollar_per_kg = (cost / pld_mass_total) if pld_mass_total > 0.5 else float("inf")
        dollar_per_kg_str = fmt_k(dollar_per_kg) if pld_mass_total > 0.5 else "—"

        row = [
            label,
            str(N),
            fmt(r["AR"]),
            fmt(chord, 2),
            fmt(struct_ac),
            fmt(pld_power_ac),
            fmt(batt_ac),
            fmt(ac_mass),
            fmt(fleet_mass),
            fmt(pld_power_total),
            fmt_k(cost),
            dollar_per_kg_str,
        ]
        lines.append("| " + " | ".join(row) + " |")

    lines.append("")

    # ── 3. Position Strategy ─────────────────────────────────────────────────
    lines.append("## Position Strategy")
    lines.append("")
    lines.append("Comparison for 4×20m formation (V, 10% overlap, 30°N summer solstice):")
    lines.append("")
    lines.append("| Strategy | Description | Expected effect |")
    lines.append("| --- | --- | --- |")
    lines.append("| heavy_wake | Leader (lightest) up front; heavy nav aircraft in best wake slot | Max drag reduction for most power-hungry aircraft |")
    lines.append("| heavy_front | Heavy aircraft leads (conventional) | Leader gets no wake benefit; baseline penalty |")
    lines.append("| uniform | All aircraft identical mass/hardware | No position assignment needed; simpler control |")
    lines.append("")
    lines.append(
        "The solver does not expose position strategy directly — `find_max_payload` always uses the V-formation "
        "drag factors from `per_slot_drag_factor`. The qualitative effect is that `heavy_wake` maximises "
        "available payload by reducing the drag budget of the most power-limited aircraft. "
        "See `wingz/aerodynamics/formation_aero.py` and `wingz/evaluation/solver.py` for implementation."
    )
    lines.append("")

    # ── 4. Model Assumptions ─────────────────────────────────────────────────
    lines.append("## Model Assumptions")
    lines.append("")
    lines.append(f"| Parameter | Value | Notes |")
    lines.append(f"| --- | --- | --- |")
    lines.append(f"| Stall margin (day) | {STALL_MARGIN_DAY} | V_cruise / V_stall |")
    lines.append(f"| Stall margin (night) | {STALL_MARGIN_NIGHT} | Near-stall in calm stratosphere |")
    lines.append(f"| Panel efficiency | {PANEL_EFFICIENCY*100:.0f}% | MicroLink III-V ELO, flight-proven on Zephyr/PHASA-35 |")
    lines.append(f"| Panel coverage | {PANEL_COVERAGE*100:.0f}% | Upper wing, excl. ailerons/spar caps/root |")
    lines.append(f"| Propulsion efficiency | {PROPULSION_EFFICIENCY*100:.0f}% | Motor × propeller × ESC |")
    lines.append(f"| Battery density | {BATTERY_ENERGY_DENSITY:.0f} Wh/kg | Cell-level Li-ion; aerospace pack ~10% lower |")
    lines.append(f"| CD0 | {CD0} | Zero-lift drag (wing only) |")
    lines.append(f"| Oswald e | {OSWALD_E} | Span efficiency factor |")
    lines.append(f"| Latitude | {DEFAULT_LATITUDE}°N | |")
    lines.append(f"| Day of year | {DEFAULT_DAY_OF_YEAR} | Summer solstice (~21 Jun) |")
    lines.append(f"| Structural model | Beam | `wingz/structures/beam.py` |")
    lines.append(f"| Dawn SOC | 0 | Battery empty at dawn (conservative sizing) |")
    lines.append(f"| Station-keeping | {N_SERVOS} servos × {SERVO_POWER_ACTIVE}W, base duty {SERVO_BASE_DUTY} | ~3.8W/follower light turbulence |")
    lines.append("")

    # ── 5. Caveats ───────────────────────────────────────────────────────────
    lines.append("## Caveats")
    lines.append("")
    lines.append("- Wing mass data is all estimated (derived from published MTOW and typical structural fractions)")
    lines.append("- Formation drag uses V-formation classical wake model; no vortex lattice or CFD")
    lines.append("- Cost model uses 2024-2026 aerospace market data; will evolve as better data arrives")
    lines.append("- Solar irradiance uses simplified analytical model (not full radiative transfer)")
    lines.append("- No dynamic effects modeled: gusts, control latency, formation break-up/rejoin")
    lines.append("- Position strategy (heavy_wake vs heavy_front) is not yet exposed as a sweep parameter in the solver")
    lines.append("- Battery density is cell-level; pack-level is ~10% lower (BMS, wiring, enclosure)")
    lines.append("")

    output = "\n".join(lines)
    output_path = "docs/formation_flight/results.md"
    with open(output_path, "w") as f:
        f.write(output)
    print(f"Written to {output_path}")


if __name__ == "__main__":
    main()
